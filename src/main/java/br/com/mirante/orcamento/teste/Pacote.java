package br.com.mirante.orcamento.teste;

public class Pacote {
    private String name;
    private String version;

    public Pacote(String name, String version) {
        this.name = name;
        this.version = version;
    }

    public String getName() {
        return name;
    }

    public String getVersion() {
        return version;
    }


}
